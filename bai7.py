import math
# Nhập bán kính hình 
a = int(input("Nhập bán kính hình tròn:"))
# Tính chu vi và diện tích hình tròn
chu_vi = 2 * a * math.pi
dien_tich = (a ** 2) * math.pi
#In kết quả
print("Chu vi hình tròn là", chu_vi)
print("Dien tichs hình tròn là", dien_tich)

 